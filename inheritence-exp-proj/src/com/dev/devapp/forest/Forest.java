package com.dev.devapp.forest;

public class Forest {
	private String area;
	private String region;
	private int noOfSpecies;
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public int getNoOfSpecies() {
		return noOfSpecies;
	}
	public void setNoOfSpecies(int noOfSpecies) {
		this.noOfSpecies = noOfSpecies;
	}
	
	

}
